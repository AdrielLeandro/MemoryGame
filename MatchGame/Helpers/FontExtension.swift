import Foundation
import UIKit

extension UIFont {

    static var smallRegularFont: UIFont {
        return UIFont(name: "Chalkduster", size: 15.0)!
    }

    static var mediumRegularFont: UIFont {
        return UIFont(name: "Chalkduster", size: 18)!
    }

    static var largeRegularFont: UIFont {
        return UIFont(name: "Chalkduster", size: 21)!
    }

    static var extraLargeRegularFont: UIFont {
        return UIFont(name: "Chalkduster", size: 50)!
    }
}
